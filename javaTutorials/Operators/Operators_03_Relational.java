package Operators;

public class Operators_03_Relational {

    static void main() {
        int a = 13, b = 10;

        System.out.println("a > b ==> " + (a > b));
        System.out.println("a < b ==> " + (a < b));
        System.out.println("a >= b ==> " + (a >= b));
        System.out.println("a <= b ==> " + (a <= b));
        System.out.println("a == b ==> " + (a == b));
        System.out.println("a != b ==> " + (a != b));
    }

}
